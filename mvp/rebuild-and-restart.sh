#!/bin/bash
set -e

# Цвета для вывода
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

echo -e "${BLUE}=====================================================================${NC}"
echo -e "${BLUE}             ПЕРЕСБОРКА И ПЕРЕЗАПУСК СИСТЕМЫ                         ${NC}"
echo -e "${BLUE}=====================================================================${NC}"

cd "$(dirname "$0")"

echo -e "${YELLOW}[1/5]${NC} Останавливаем контейнеры...${NC}"
docker-compose down || true

echo -e "${YELLOW}[2/5]${NC} Пересобираем образы...${NC}"
docker-compose build --no-cache tracking-service

echo -e "${YELLOW}[3/5]${NC} Запускаем систему...${NC}"
docker-compose up -d

echo -e "${YELLOW}[4/5]${NC} Ожидаем запуск контейнеров (30 секунд)...${NC}"
sleep 30

echo -e "${YELLOW}[5/5]${NC} Проверяем статус системы...${NC}"
./check-status.sh

echo -e "${GREEN}=====================================================================${NC}"
echo -e "${GREEN}                    СИСТЕМА ПЕРЕЗАПУЩЕНА                             ${NC}"
echo -e "${GREEN}=====================================================================${NC}"
